public class Aritmathic {
  public static void main(String[] args) {
    Integer a = 100;
    Integer b = 10;

    System.out.println(a + b);
    System.out.println(a - b);
    System.out.println(a / b);
    System.out.println(a * b);
    System.out.println(a % b);

    System.out.println(a += b);
    System.out.println(a -= b);
    System.out.println(a /= b);
    System.out.println(a *= b);
    System.out.println(a %= b);

    System.out.println("-------");
    System.out.println(a++);
    System.out.println(a--);
    System.out.println(-a);
    System.out.println(a %= b);

  }
}
